public class Calculator {

    /**
     * Add methods to add 2 numbers
     *
     * @param d1 first number
     * @param d2 second number
     * @return calculated sum of two numbers
     */
    public static double add(double d1, double d2) {
        return d1 + d2;
    }

    /**
     * subtract methods to subtract 2 numbers
     *
     * @param d1 first number
     * @param d2 second number
     * @return calculated subtraction of two numbers
     */
    public static double subtract(double d1, double d2) {
        return d1 - d2;
    }

    /**
     * multiply methods to multiply 2 numbers
     *
     * @param d1 first number
     * @param d2 second number
     * @return calculated multiplication of two numbers
     */
    public static double multiply(double d1, double d2) {
        return d1 * d2;
    }

    /**
     * divide methods to add 2 numbers
     *
     * @param d1 first number
     * @param d2 second number
     * @return calculated division of two numbers
     */
    public static double divide(double d1, double d2) {
        return d1 / d2;
    }


}
